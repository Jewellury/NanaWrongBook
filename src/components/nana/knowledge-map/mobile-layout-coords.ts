/**
 * 获取节点坐标，缺失时 fallback 到画布中心 + console.warn。
 * 调用方（canvas mobile 分支）必须通过此函数取值，不可直接索引 MOBILE_COORDS。
 */
export function getMobileCoord(nodeId: string): { x: number; y: number } {
  const c = MOBILE_COORDS[nodeId];
  if (!c) {
    console.warn(
      `[mobile-layout] 节点 "${nodeId}" 缺少手工坐标，fallback 到画布中心。请在 mobile-layout-coords.ts 中补充。`
    );
    return { x: MOBILE_W / 2, y: MOBILE_H / 2 };
  }
  return c;
}
